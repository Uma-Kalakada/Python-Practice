# program to perform all mathematical operations on two numbers.

a = 4
b = 2
sum = a + b
print("The sum of", a, "and", b, "is", sum)
print(f"The sum of {a} and {b} is {a+b}.")

diff = a - b
print("The difference between", a, "and", b,"is", diff)
print(f"The difference between {a} and {b} is {diff}.")

prod = a * b
print(f"The product of {a} and {b} is {prod}.")

quotient = a/b
print(f"The result of {a} divided by {b} is {quotient}.")

rem = a%b
print(f"The remainder of {a} when divided by {b} is {rem}.")

#gives integer 
floor_quotient = a//b
print(f"the result of floor division of {a} by {b} is {floor_quotient}.")

exp = a**b
print(f"The result of {a} raising to the power of {b} is {exp}.")
